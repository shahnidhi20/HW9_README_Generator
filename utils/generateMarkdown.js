// TODO: Create a function that returns the license link
// If there is no license, return an empty string
function renderLicenseLink(license) {
  let link = "https://www.boost.org/LICENSE_1_0.txt";
  if (license === "MIT") {
    return (link = "https://opensource.org/licenses/MIT");
  } else if (license === "Apache") {
    return (link = "https://opensource.org/licenses/Apache-2.0");
  } else if (license === "Mozilla") {
    return (link = "https://opensource.org/license/mit/");
  } else if (license === "npm") {
    return (link = "https://docs.npmjs.com/policies/npm-license");
  } else if (license === "inquirer") {
    return (link = "https://www.npmjs.com/package/inquirer");
  } else if (license === "node") {
    return (link = "https://nodejs.org/en/download");
  } else {
    return link;
  }
}

// TODO: Create a function that returns the license section of README
// If there is no license, return an empty string
function renderLicenseSection(license) {
  if (license === "") return "";
  else {
    var link = renderLicenseLink(license);
    var badge = renderLicenseBadge(license);
    var licenseBadge = `[![License:](${badge})](${link})`;
    return licenseBadge;
  }
}

