// TODO: Create a function that returns a license badge based on which license is passed in
// If there is no license, return an empty string
function renderLicenseBadge(license) {
  let badge = "https://img.shields.io/badge/License-Boost%201.0-lightblue.svg";

  if (license === "") return "";

  if (license === "MIT") {
    return (badge = "https://img.shields.io/badge/License-MIT-yellow.svg");
  } else if (license === "Apache") {
    return (badge =
      "https://img.shields.io/badge/License-Apache%202.0-blue.svg");
  } else if (license === "Mozilla") {
    return (badge =
      "https://img.shields.io/badge/License-MPL_2.0-brightgreen.svg");
  } else if (license === "npm") {
    return (badge = "https://img.shields.io/badge/npm-npm-brightgreen");
  } else if (license === "node") {
    return (badge = "https://img.shields.io/node/v/gh-badges.svg");
  } else if (license === "inquirer") {
    return (badge = "https://img.shields.io/badge/inquirer-8.2.4-blue");
  } else {
    return badge;
  }
}

